%% dsBoxPlot
% Box plot for a dataset
%% Syntax
% * 		dsBoxPlot(DS)
%% Description
% 		dsBoxPlot(DS) plots the features of each class as a box plot to show the features' distributions.
%% Example
%%
%
DS=prData('iris');
dsBoxPlot(DS);
